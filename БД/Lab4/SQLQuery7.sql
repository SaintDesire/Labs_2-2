use Kor_MyBase;
SELECT Кредиты.ID_Кредита, Кредиты.Дата_выдачи, Кредиты.Сумма,
CASE
WHEN Кредиты.Сумма BETWEEN 1000 AND 5000 THEN 'Малый кредит'
WHEN Кредиты.Сумма BETWEEN 5001 AND 10000 THEN 'Средний кредит'
WHEN Кредиты.Сумма > 10000 THEN 'Большой кредит'
ELSE 'Нет кредита'
END AS Размер_кредита,
Клиенты.Название_фирмы_клиента
FROM Кредиты
INNER JOIN Клиенты ON Кредиты.ID_Клиента = Клиенты.ID_Клиента
WHERE Кредиты.Дата_выдачи BETWEEN '2022-01-01' AND '2022-12-31';