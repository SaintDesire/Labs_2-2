use Kor_MyBase;
SELECT Кредиты.ID_Кредита, [Типы кредитов].Название_кредита
FROM Кредиты
INNER JOIN [Типы кредитов] ON Кредиты.ID_Типа_кредита = [Типы кредитов].ID_Типа_кредита;


SELECT Кредиты.ID_Кредита, Клиенты.Название_фирмы_клиента
FROM Кредиты
INNER JOIN Клиенты
ON Кредиты.ID_Клиента = Клиенты.ID_Клиента
WHERE Клиенты.Название_фирмы_клиента LIKE '%ООО%';
